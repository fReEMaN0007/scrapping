package com.angelowkey.lotto.model;

public class PreviousResults {
	String draw_type;
	String combination;
	String draw_date;
	String jackpot;
	String winners;
	
	public String getDraw_type() {
		return draw_type;
	}
	public void setDraw_type(String draw_type) {
		this.draw_type = draw_type;
	}
	public String getCombination() {
		return combination;
	}
	public void setCombination(String combination) {
		this.combination = combination;
	}
	public String getDraw_date() {
		return draw_date;
	}
	public void setDraw_date(String draw_date) {
		this.draw_date = draw_date;
	}
	public String getJackpot() {
		return jackpot;
	}
	public void setJackpot(String jackpot) {
		this.jackpot = jackpot;
	}
	public String getWinners() {
		return winners;
	}
	public void setWinners(String winners) {
		this.winners = winners;
	}


}
